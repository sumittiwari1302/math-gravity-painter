export interface StarPos { id: string; x: number; y: number }
export interface Platform { x: number; y: number; w: number; h: number }
export interface LevelDef {
  id: string;
  worldId: number;
  levelNumber: number;
  difficulty: "veryEasy" | "easy" | "medium";
  ballStart: { x: number; y: number };
  portalPos: { x: number; y: number };
  starPositions: StarPos[];
  platforms: Platform[];
  mathChallengeIndex: number;
}

function mkLevel(wid: number, ln: number, bx: number, by: number, px: number, py: number, stars: [number,number][], plats: [number,number,number,number][]): LevelDef {
  const diff: LevelDef["difficulty"] = ln <= 5 ? "veryEasy" : ln <= 10 ? "easy" : "medium";
  return {
    id: `w${wid}l${ln}`,
    worldId: wid,
    levelNumber: ln,
    difficulty: diff,
    ballStart: { x: bx, y: by },
    portalPos: { x: px, y: py },
    starPositions: stars.map((s, i) => ({ id: `s${i}`, x: s[0], y: s[1] })),
    platforms: plats.map(p => ({ x: p[0], y: p[1], w: p[2], h: p[3] })),
    mathChallengeIndex: ln - 1,
  };
}

